class Address {
  final int? id;
  final String? street;
  final String? streetName;
  final String? buildingNumber;
  final String? city;
  final String? zipcode;
  final String? country;
  final String? countyCode;
  final double? latitude;
  final double? longitude;

  Address(
      this.id,
      this.street,
      this.streetName,
      this.buildingNumber,
      this.city,
      this.zipcode,
      this.country,
      this.countyCode,
      this.latitude,
      this.longitude);
}
